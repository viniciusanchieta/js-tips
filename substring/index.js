/* 
	O método recebe dois parâmetros, substring(inicio, fim).
	Para extrair, imagine que cada caractere é uma posição.
	O primeiro parâmetro é a posição inicial.
	O segundo parâmetro é a posição final.
*/

const string = "Desenvolvimento";

// Vamos limitar a exibição da string, da posição 0 até a posição 5
console.log(string.substring(0, 5)); // Desen

// Agora vamos exibir do posição 5 até o final
console.log(string.substring(5, 15)); // volvimento
// Ou
console.log(string.substring(5)); // volvimento



